drop table Account, Thesis, Team, Task, Comment, Evaluation, Notification, ThesisStatus, Technology

-- CREATE TABLE ACCOUNT
create table Account (
    idaccount nvarchar(10) primary key not null,
    fullname nvarchar(50) not null,
    citizencode nvarchar(15) not null,
    birthday datetime not null,
    gender nvarchar(10) not null,
    email nvarchar(50) not null,
    phonenumber nvarchar(15) not null,
    handle nvarchar(50) not null,
    role nvarchar(10) not null,
    university nvarchar(100) not null,
    faculty nvarchar(50) not null,
    workcode nvarchar(20) not null,
    password nvarchar(50) not null,
    avatarname varchar(30) not null,
);

-- CREATE TABLE THESIS
create table Thesis (
    idthesis nvarchar(10) primary key not null,
    topic text not null,
    field nvarchar(50) not null,
    tslevel nvarchar(10) not null,
    maxmembers int not null,
    description text not null,
    publishdate datetime not null,
    technology text not null,
    functions text not null,
    requirements text not null,
    idcreator nvarchar(10) not null,
    isfavorite bit not null default 0,
    status nvarchar(10) not null,
	idinstructor nvarchar(10) not null,
);

-- CREATE TABLE TEAM
create table Team
(
    idteam nvarchar(10) not null,
    idaccount nvarchar(10) not null,
    name nvarchar(50) not null,	
    created datetime not null,
    avatarname varchar(50) not null,
);

-- CREATE TABLE TASK
create table Task
(
	idtask nvarchar(10) not null,
	title text not null,
	description text not null,
	idcreator nvarchar(10) not null,
	idteam nvarchar(10) not null,
	isfavorite bit not null default 0,
	progress int not null,
	created datetime not null,
);

-- CREATE TABLE COMMENT
create table Comment
(
	idcomment nvarchar(10) not null,
	idtask nvarchar(10) not null,
	idcreator nvarchar(10) not null,
	content text not null,
	emoji varchar(20) not null,
	created datetime not null,
);

-- CREATE TABLE EVALUATION
create table Evaluation
(
	idevaluation nvarchar(10) not null,
	idtask nvarchar(10) not null,
	idpeople nvarchar(10) not null,
	content text,
	contribute int not null,
	scores float not null,
	created datetime not null,
	isevaluated bit not null default 0,
);

-- CREATE TABLE NOTIFICATION
create table Notification
(
	idnotification nvarchar(10) not null,
	idhost nvarchar(10) not null,
	idsender nvarchar(10) not null,
	idobject nvarchar(10) not null,
	content text not null,
	type nvarchar(15) not null,
	created datetime not null,
	isfavorite bit not null default 0,
	issaw bit not null default 0,
);

